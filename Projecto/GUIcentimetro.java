    private void jButtonAddParagemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddParagemActionPerformed
        // TODO add your handling code here:
        String nome=this.jTextFieldNomeParagemNova.getText();
        String lat=this.jTextFieldLatitudeParagem.getText();
        Double c=Double.parseDouble(lat);
        String longi=this.jTextFieldLongitudeParagem.getText();
        Double d=Double.parseDouble(longi);

        Zona zonaSelected = null;

        for(Zona z : Rede.zonas){
            if(z.getNome().compareTo(this.jComboBoxZonaNovaParagem.getSelectedItem().toString())==0){
                zonaSelected=z;
                break;
            }
        }
        Paragem p = new Paragem(nome,new Localizacao(c,d),zonaSelected);
        stops.add(p);

        try {
            zonaSelected.associarParagem(p);
        } catch (ParagemExistenceException ex) {
            ex.printStackTrace();
        }

        //System.out.println(stops.get(0).getNome());
        String[] toppings = new String[stops.size()];
        for(int i=0;i< stops.size();i++){
            toppings[i] = stops.get(i).getNome();
        }

        this.jComboBoxOrigemLig.setModel(new javax.swing.DefaultComboBoxModel(toppings));
        this.jComboBoxDestinoLig.setModel(new javax.swing.DefaultComboBoxModel(toppings));
        this.jComboBoxNomeParagem.setModel(new javax.swing.DefaultComboBoxModel(toppings));
        this.jComboBoxParagemOrigem.setModel(new javax.swing.DefaultComboBoxModel(toppings));
        this.jComboBoxParagemDestion.setModel(new javax.swing.DefaultComboBoxModel(toppings));
    }//GEN-LAST:event_jButtonAddParagemActionPerformed

